let preNav = "smiley";
let currentNav = "smiley";

function createPage(nav) {
    const cardContainer = document.querySelector(".card-container");
    cardContainer.innerHTML = "";

    let items;
    preNav = currentNav;
    switch (nav) {
        case "all":
            items = all;
            currentNav = "all";
            break;
        case "smiley":
            items = smiley;
            currentNav = "smiley";
            break;
        case "food":
            items = food;
            currentNav = "food";
            break;
        case "handGesture":
            items = handGesture;
            currentNav = "handGesture";
            break;
        case "animal":
            items = animal;
            currentNav = "animal";
            break;
        case "nature":
            items = nature;
            currentNav = "nature";
            break;
        case "activity":
            items = activity;
            currentNav = "activity";
            break;
        case "people":
            items = people;
            currentNav = "people";
            break;
        case "travelAndPlace":
            items = travelAndPlace;
            currentNav = "travelAndPlace";
            break;
        case "object":
            items = object;
            currentNav = "object";
            break;
        case "symbol":
            items = symbol;
            currentNav = "symbol";
            break;
        case "flag":
            items = flag;
            currentNav = "flag";
            break;
        default:
            console.log("Cannot found nav (${nav})");
            return;
    }

    document.getElementById(preNav).style.backgroundColor = 'rgb(35, 130, 255)';
    document.getElementById(currentNav).style.backgroundColor = 'rgb(255, 61, 61)';
    for (var i = 0; i < items.length; i++) {
        const div = document.createElement("div");
        div.className = "card";
        div.innerHTML = `
        <div class="card-text">${items[i]}</div>
        <div class="darkness" onclick="copyStringToClipboard('${items[i]}')">COPY</div>
        `
        cardContainer.appendChild(div);
    }
}

function copyStringToClipboard (str) {
    console.log(str + " copied!");
    var el = document.createElement('textarea');
    el.value = str;
    el.setAttribute('readonly', '');
    el.style = {position: 'absolute', left: '-9999px'};
    document.body.appendChild(el);
    el.select();
    document.execCommand('copy');
    document.body.removeChild(el);
    notification(str);
}

function notification(str) {
    const noti = document.createElement('div');
    noti.className = "noti";
    noti.innerText = str + " Copied!";
    document.body.appendChild(noti);
    setTimeout(() => {
        document.body.removeChild(noti);
    }, 1000);
}

function createTabEvent() {
    const tabs = document.getElementsByClassName('tab');
    for (let i = 0; i < tabs.length; i++) {
        tabs[i].addEventListener('click', () => {
            createPage(tabs[i].id);
        });
    }
}

window.onload = () => {
    createPage("all");
    createTabEvent();
}